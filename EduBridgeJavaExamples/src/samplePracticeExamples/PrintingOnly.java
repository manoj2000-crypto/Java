package samplePracticeExamples;

public class PrintingOnly {

	public static void main(String[] args) {

		//Printing 10 times
		for(int i=1; i<=10; i++) 
		{
			System.out.println("java developer:1");
		}
		
		
		//printing infinite times
		for(int i =10; i>=0; i++) 
		{
			System.out.println("java developer:1");
		}
		
	}

}
